/**
 * Returns the display name for dev/admin logs.
 * Returns null in production for non-admin contexts to prevent leakage.
 */
export function getPersonaLogLabel(
  id: AgentPersonaId,
  opts: { isAdmin?: boolean } = {}
): string | null {
  if (process.env.NODE_ENV !== "production" || opts.isAdmin) {
    return AGENT_PERSONAS[id].name;
  }
  return null;
}

/**
 * Returns a persona's system prompt block for injection into agent prompts.
 * Always safe to call server-side — this content is internal only.
 */
export function getPersonaPromptBlock(id: AgentPersonaId): string {
  return AGENT_PERSONAS[id].systemPromptBlock;
}
