// ─── Agent Personas ───────────────────────────────────────────────────────────
//
// Internal persona definitions for TrainChat's three-agent system.
//
// These personas are INTERNAL-ONLY. They shape agent behavior, tone, and
// specialization. They are NEVER exposed to normal users — the user-facing
// product is always "TrainChat."
//
// Personas may appear in:
//   - Dev/admin logs (NODE_ENV !== "production" || isAdminRequest)
//   - Internal system prompts (injected as identity headers)
//   - Orchestrator debug output
//
// They must NEVER appear in:
//   - User-facing chat responses
//   - Program output panels
//   - Error messages shown to users
//   - API response bodies sent to non-admin clients
// ─────────────────────────────────────────────────────────────────────────────

export type AgentPersonaId =
  | "coach_atlas"       // User-facing Coach Agent (lib/ai.ts)
  | "architect_vale"    // Internal Performance Architect (program-architecture-engine.ts)
  | "dr_sable";         // Internal Research Librarian (research/research-librarian-agent.ts)

export interface AgentPersona {
  id: AgentPersonaId;
  /** Internal codename — dev/admin logs only */
  name: string;
  /** Short role description for logging/debugging */
  role: string;
  /**
   * System prompt persona block injected at the top of this agent's prompt.
   * Establishes internal identity, tone, and specialization.
   * NEVER shown to users.
   */
  systemPromptBlock: string;
  /** Hard rules this persona must never violate */
  hardRules: string[];
}

